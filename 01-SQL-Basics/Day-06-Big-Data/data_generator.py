import random
import datetime

# --- KONFIGURASI ---
JUMLAH_DATA = 100  # Mau bikin berapa data? Ganti jadi 1000 atau 5000 kalau berani!
FILENAME = "bulk_data.sql"

# --- DATA DUMMY (Bahan Baku) ---
names = ["Budi", "Siti", "Agus", "Dewi", "Rian", "Nina", "Eko", "Fani", "Gilang", "Hani"]
