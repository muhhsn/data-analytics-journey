        # 1. Generate Orders
        f.write("INSERT INTO orders (customer_id, order_date, total_amount, payment_method) VALUES \n")
        
        for i in range(JUMLAH_DATA):
            # Acak Data
            cust_id = random.randint(1, 5) # Asumsi kita punya 5 customer dulu
            prod = random.choice(products)
            amount = prod[1] # Ambil harga
            
            # Acak Tanggal (Dalam 30 hari terakhir)
            days_offset = random.randint(0, 30)
            date_obj = datetime.date.today() - datetime.timedelta(days=days_offset)
            date_str = date_obj.strftime("%Y-%m-%d")
            
            pay = random.choice(payments)
            
            # Format SQL
            row = f"({cust_id}, '{date_str}', {amount}, '{pay}')"
            
            if i < JUMLAH_DATA - 1:
                f.write(row + ",\n") # Koma untuk data selanjutnya
            else:
                f.write(row + ";\n") # Titik koma untuk data terakhir
        
